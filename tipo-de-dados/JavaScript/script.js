console.log(typeof 1234); // integer
console.log(12.34); // float
console.log(typeof 12.34); // float
console.log(1.2e3); // 1.2 * 10^3
console.log(typeof 1.2e3); // 1.2 * 10^3
console.log(-43); // 1.2 * 10^-3
console.log(typeof -43); // 1.2 * 10^3

